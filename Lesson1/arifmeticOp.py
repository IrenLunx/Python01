# Арифметические операции
# / - деление на вещественные числа, // - деление на целые числа
# % - остаток от деления
# ** - возведение в степень

l = 5.5
h = 6
m = l // h
print(m)


m = round(l * h, 3) # для выведения определенного количества знаков, если не указывать количество, то округлит согласно законам математики
print(m)